echo "Baris\nbaru <br>"; // soal 10a
echo "Baris\nbaru <br>"; // soal 10b
echo "Halo\rDunia <br>"; // soal 10c
echo "Baris\rDunia <br>"; // soal 10d

// menampilkan teks sesuai dengan yang diinputkan dan diikuti dengan tag <pre> 
echo "<pre>Halo\tDunia!</pre>"; // soal 10e
echo '<pre>Halo\tDunia!</pre>'; // soal 10f

// menampilkan teks sesuai dengan yang diinputkan dengan menggunakan karakter escape (\)
// untuk melarang interpretasi tanda kutip ganda di dalam string.
echo "Katakanlah \"Tidak Pada Narkoba!\"<br>"; // soal 10g
echo 'Katakanlah \'Tidak Pada Narkoba!\'<br>'; // soal 10h

?>

